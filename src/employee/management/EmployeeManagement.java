    private void button_addEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_addEmployeeActionPerformed
        // TODO add your handling code here:
        employeeName = text_e_name.getText();
        if(radio_finance.isSelected()){
            employeeDuty = new Finance().getTask();
        }else if(radio_store.isSelected()){
            employeeDuty = new Store().getTask();
        }
        
        if(radio_wageLevel_A.isSelected()){
            employeePayment = new WageLevel_A().getSalary();
        }else if(radio_wageLevel_B.isSelected()){
            employeePayment = new WageLevel_B().getSalary();
        }
        
        if(employeeName != null && employeeDuty != null && employeePayment != 0){
//            new Employees(employeeName, employeeDuty, employeePayment);
            if (new DBUpdater().addEmployee(employeeName, employeeDuty, employeePayment)) {
            text_e_name.setText("");
            DefaultTableModel de = new DBUpdater().getData();
            jTable1.setModel(de);
            } else {
                JOptionPane.showMessageDialog(null, "Employee not saved");
            }
        }else{
            showMessageDialog(null, "Please fill all details.");
        }
    }//GEN-LAST:event_button_addEmployeeActionPerformed

