    private void button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_deleteActionPerformed
        // TODO add your handling code here:
        String[] options = {"Yes", "No"};
        int ans = JOptionPane.showOptionDialog(null, "Delete?", "Delete confirm", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[1]);
        if (ans == 0) {
            int index = jTable1.getSelectedRow();
            String id = jTable1.getValueAt(index, 0).toString();
            if (new DBUpdater().delete(id)) {
//                JOptionPane.showMessageDialog(null, "Deleted");
                text_e_name.setText("");
                DefaultTableModel de = new DBUpdater().getData();
                jTable1.setModel(de);
            } else {
                JOptionPane.showMessageDialog(null, "not deleted");
            }
        }
    }//GEN-LAST:event_button_deleteActionPerformed

